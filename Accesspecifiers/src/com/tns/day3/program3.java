public class program3 {
	
	public static void main(String[] args) 
	{
	
		int x=10;
		
		if(x==1)
		{
			System.out.println("x value is 1");  // when x value is 1 then this output is printed
		}
		else if(x==2)
		{
			System.out.println("x value is 2");  // when x value is 2 then this output is printed
		}
		else if(x==3)
		{
			System.out.println("x value is 3");  // when x value is 3 then this output is printed
		}
		else
		{
			System.out.println("x value is other than 1,2,3");// when x value is other than 1,2,3 then this output is printed
		}
		
	}

}

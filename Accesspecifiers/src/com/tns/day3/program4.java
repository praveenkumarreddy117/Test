package com.tns.day3;

public class program4 {
	
	public static void main(String[] args) {
		
		//when we are using || operator if any one condition satisfies results will be printed
		
		int x=10;
		int y=20;
		
		if((x<y) || (x==y))
		{
			System.out.println("true"); // when y is greater than x then this output is printed
		}
		else
		{
			System.out.println("false"); // when y is less than x then this output is printed
		}
		
	}

}

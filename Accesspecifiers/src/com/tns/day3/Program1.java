package com.tns.day3;

public class Program1 {
	
	public static void main(String[] args) {
		
		int x = 10;
		int y = 20;
		
		System.out.println(x);
		
		System.out.println(y);
		
		System.out.println(x+y);
		
		System.out.println(x-y);
		
		System.out.println(++x);
		
		System.out.println(--y);
		
	}

}
